#include <unistd.h>

void	swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;

	if (size < 2)
		return ;
	i = 0;
	while (i < --size)
	{
		swap(&tab[i++], &tab[size]);
	}
}
